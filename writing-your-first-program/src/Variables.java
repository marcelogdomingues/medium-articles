
/*
    Variables are used to store data in a program. In Java, variables are declared using a data type followed by a name.
    The data type specifies the type of data that the variable can store. For example, int is a data type that can store integer values.
    The name of the variable is used to refer to the data stored in the variable.
    In the example below, we declare two variables: number and message. The number variable stores an integer value, and the message variable stores a string value.
    We then print the values stored in the variables using the System.out.println() method.
 */

public class Variables {
    public static void main(String[] args) {
        int number = 10; // Integer variable
        String message = "Hello, Java!"; // String variable
        System.out.println("Number: " + number);
        System.out.println("Message: " + message);
    }
}